// Clase para representar una cita
class Cita {

    private String nombreEmpresa;
    private int cantidadExtintores;
    private Date fechaCita;
    private String horario;
    private int costoTotal;

    // MÃ©todo para calcular el costo total
    private void calcularCostoTotal() {
        costoTotal = cantidadExtintores * 480;
    }

    public Cita(String nombreEmpresa, int cantidadExtintores, Date fechaCita, String horario) {
        this.nombreEmpresa = nombreEmpresa;
        this.cantidadExtintores = cantidadExtintores;
        this.fechaCita = fechaCita;
        this.horario = horario;
        calcularCostoTotal(); // Calcula el costo total al crear la cita
    }

    public String getNombreEmpresa() {
        return nombreEmpresa;
    }

    public int getCantidadExtintores() {
        return cantidadExtintores;
    }

    public Date getFechaCita() {
        return fechaCita;
    }

    public String getHorario() {
        return horario;
    }

    public int getCostoTotal() {
        return costoTotal;
    }
}

