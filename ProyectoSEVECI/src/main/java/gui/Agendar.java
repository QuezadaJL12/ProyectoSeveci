    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
    if (textFieldNombreEmpresa.getText().isEmpty()
            || textFieldCantidadExt.getText().isEmpty()
            || JDCCita.getDate() == null
            || JCBHora.getSelectedItem() == null
            || JCBMinutos.getSelectedItem() == null) {
        JOptionPane.showMessageDialog(this, "Los campos no estÃ¡n completos.", "ERROR", JOptionPane.ERROR_MESSAGE);
        return;
    }

    try {
        int cantidad = Integer.parseInt(textFieldCantidadExt.getText());
        if (cantidad > 100) {
            JOptionPane.showMessageDialog(this, "Cantidad mÃ¡xima de 100 extintores", "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Crear la cita con los datos ingresados
        Cita nuevaCita = new Cita(
                textFieldNombreEmpresa.getText(),
                cantidad,
                JDCCita.getDate(),
                JCBHora.getSelectedItem().toString()
                + ":" + JCBMinutos.getSelectedItem().toString()
        );

        // Agregar la cita a la lista de citas
        citas.add(nuevaCita);

        // Mostrar un mensaje de Ã©xito
        JOptionPane.showMessageDialog(this, "Cita agendada correctamente.", "Ãxito", JOptionPane.INFORMATION_MESSAGE);

        // Crear una instancia de la ventana Ticket y hacerla visible
        Ticket ticket = new Ticket();
        ticket.setVisible(true);
        dispose(); // Cerrar la ventana actual (Agendar)

        // Actualizar los datos en la ventana Ticket
        ticket.actualizarDatos(
                nuevaCita.getNombreEmpresa(),
                Integer.toString(nuevaCita.getCantidadExtintores()),
                new SimpleDateFormat("dd/MM/yyyy").format(nuevaCita.getFechaCita()),
                nuevaCita.getHorario(),
                Integer.toString(nuevaCita.getCostoTotal())
        );
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Ingrese un nÃºmero vÃ¡lido para la cantidad de extintores", "ERROR", JOptionPane.ERROR_MESSAGE);
    }


    }//GEN-LAST:event_btnAceptarActionPerformed

